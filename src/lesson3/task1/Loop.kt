fun fibSequenceDigit(n: Int): Int {
    var count = 1
    var number1 = 0
    var number2 = 0
    var numbers = 1
    while (count < n) {
        number1 = number2
        number2 = numbers
        numbers = number1 + number2
        count += digitNumber(numbers)
    }
    if (count == n) return numbers % 10
    else {
        var repeat = count - n
        while (repeat > 0) {
            numbers /= 10
            repeat -= 1
        }
        return numbers % 10
    }
}



